public class ArrayUniDemo {
   public static void main (String args[]) {
     // declara array unidimensional
     int[] array = {1,2,3,4,5};
     //
     int x = array[2];
      System.out.println("Index 1 = " + array[0]);
      System.out.println("Index 2 = " + array[1]);
      System.out.println("Index 3 = " + array[2]);
      System.out.println("Index 4 = " + array[3]);
      System.out.println("Index 5 = " + array[4]);
      System.out.println("x = " + x);
   }
}
